a = float(input("Enter a number: "))
b = float(input("Enter an another number: "))

print(str(a) + " + " + str(b) + " = " + str(a + b))
print(str(a) + " - " + str(b) + " = " + str(a - b))
print(str(a) + " * " + str(b) + " = " + str(a * b))
print(str(a) + " / " + str(b) + " = " + str(a // b))



print(a, "+" ,b, "=" ,a + b)
print(a, "-" ,b, "=" ,a - b)
print(a, "*" ,b, "=" ,a * b)
print(a, "/" ,b, "=" ,a // b)


